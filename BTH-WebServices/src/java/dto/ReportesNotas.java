public class ReportesNotas {
    
    private int IdColegios;
    private String NombreColegio;
    private int IdGrupos;
    private String Nivel;
    private int IdNotas;
    private String Notas;
    private String Estudiante ;
    private int IdCarreras;
    private String Carrera;

    public ReportesNotas() {
 
    }

    public int getIdColegios() {
        return IdColegios;
    }

    public void setIdColegios(int IdColegios) {
        this.IdColegios = IdColegios;
    }

    public String getNombreColegio() {
        return NombreColegio;
    }

    public void setNombreColegio(String NombreColegio) {
        this.NombreColegio = NombreColegio;
    }

    public int getIdGrupos() {
        return IdGrupos;
    }

    public void setIdGrupos(int IdGrupos) {
        this.IdGrupos = IdGrupos;
    }

    public String getNivel() {
        return Nivel;
    }

    public void setNivel(String Nivel) {
        this.Nivel = Nivel;
    }

    public int getIdNotas() {
        return IdNotas;
    }

    public void setIdNotas(int IdNotas) {
        this.IdNotas = IdNotas;
    }

    public String getNotas() {
        return Notas;
    }

    public void setNotas(String Notas) {
        this.Notas = Notas;
    }

    public String getEstudiante() {
        return Estudiante;
    }

    public void setEstudiante(String Estudiante) {
        this.Estudiante = Estudiante;
    }

    public int getIdCarreras() {
        return IdCarreras;
    }

    public void setIdCarreras(int IdCarreras) {
        this.IdCarreras = IdCarreras;
    }

    public String getCarrera() {
        return Carrera;
    }

    public void setCarrera(String Carrera) {
        this.Carrera = Carrera;
    }

    
    
    
    
}
